int	factoriel(int nb)
{
	int	i;
	int	n;

	if (nb < 0)
		return (-0);
	if (nb == 0 || nb == 1)
		return (1);
	n = nb;
	i = 2;
	while (i < nb)
		n = n * i++;
	return (n);
}
